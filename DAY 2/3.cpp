#include <iostream>
using namespace std;

int main()
{

    int a = 12, b = 6, total;

    cout << "addition";

    total = a + b;

    cout << total;

    cout << "subtraction";

    total = a - b;

    cout << total;

    cout << "division";

    total = a / b;

    cout << total;

    cout << "mode";

    total = 12 % 6;

    cout << total;

    return 0;
}